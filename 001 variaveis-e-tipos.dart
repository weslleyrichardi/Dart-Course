// VARIAVEIS


void main() {

  String nome = "Richard"; // Declara uma variável String e a inicializa com "Richard"
  print(nome); // Imprime o valor da variável nome

  int number = 10; // Declara uma variável inteira e a inicializa com 10
  print(number); // Imprime o valor da variável number

  bool boolean = true; // Declara uma variável booleana e a inicializa com true
  print(boolean); // Imprime o valor da variável boolean

  List<String> listaDeNomes = ["David", "Wylian"]; // Declara uma lista de Strings e a inicializa com dois nomes
  print('${listaDeNomes[0]} e ${listaDeNomes[1]}'); // Imprime os elementos da lista no formato "David e Wylian"
}
